CREATE OR REPLACE FUNCTION "public"."generate_painter_name"() RETURNS "text"
    LANGUAGE "plpgsql"
    AS $$
declare
  candidate text;
  attempts int := 0;
begin
  loop
    candidate := 'Painter' || lpad(floor(random() * 10000)::int::text, 4, '0');

    -- Check if display_name already exists (case-insensitive)
    if not exists (
      select 1 from public.profiles where lower(display_name) = lower(candidate)
    ) then
      return candidate;
    end if;

    attempts := attempts + 1;
    if attempts > 100 then
      raise exception 'Could not generate a unique display name after 100 attempts';
    end if;
  end loop;
end;
$$;


ALTER FUNCTION "public"."generate_painter_name"() OWNER TO "postgres";


CREATE OR REPLACE FUNCTION "public"."handle_new_user"() RETURNS "trigger"
    LANGUAGE "plpgsql" SECURITY DEFINER
    AS $$
begin
  insert into public.profiles (id, display_name, avatar_url)
  values (
    new.id,
    public.generate_painter_name(),
    new.raw_user_meta_data ->> 'avatar_url'
  );
  return new;
end;
$$;


ALTER FUNCTION "public"."handle_new_user"() OWNER TO "postgres";


